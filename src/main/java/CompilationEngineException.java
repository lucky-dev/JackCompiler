public class CompilationEngineException extends Exception {

    public CompilationEngineException() {
    }

    public CompilationEngineException(String message) {
        super(message);
    }

    public CompilationEngineException(Throwable cause) {
        super(cause);
    }

    public CompilationEngineException(String message, Throwable cause) {
        super(cause);
    }

}
